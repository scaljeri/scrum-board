$(function () {
    $('body').click(function (e) {
        var el = $(e.target),
            list = App.outsideClick.list;
        App.outsideClick.isDirty = false;

        for (var i = 0; i < list.length; i++) {
            if (el.closest(list[i].selector).length === 0) {
                if (list[i].ignore) {
                    list[i].ignore = false;
                }
                else {
                    list[i].callback(e);
                }
            }
        }
    });
});

